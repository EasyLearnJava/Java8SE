package com.easylearnjava.constructorchaining;

public class Demo {

	public Demo(){		
		System.out.println("In Demo class default constructor...");
	}
	
	public Demo(int x){
		System.out.println("In Demo class 1 parameter constructor...");
	}
	
	public Demo(String x, float y){
		System.out.println("In Demo class with 2 parameter constructor...");
	}
	
}
